import { mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const sourcePath = path.join(root, 'content', 'lightningchart-js', 'SKILL.md')
const targetPath = path.join(root, 'src', 'skill.generated.ts')

const skill = await readFile(sourcePath, 'utf8')
const output = `// This file is generated by scripts/generate-worker-content.mjs.\nexport const lightningChartSkillMarkdown = ${JSON.stringify(skill)}\n`

await mkdir(path.dirname(targetPath), { recursive: true })
await writeFile(targetPath, output)

